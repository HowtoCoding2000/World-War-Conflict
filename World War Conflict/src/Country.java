
public class Country
	{
		String name;
		int numberOfNukes;
		int numberOfLaserSystems;
		int population;
		
		public String getName()
			{
				return name;
			}
		public void setName(String name)
			{
				this.name = name;
			}
		public int getNumberOfNukes()
			{
				return numberOfNukes;
			}
		public void setNumberOfNukes(int numberOfNukes)
			{
				this.numberOfNukes = numberOfNukes;
			}
		public int getNumberOfLaserSystems()
			{
				return numberOfLaserSystems;
			}
		public void setNumberOfLaserSystems(int numberOfLaserSystems)
			{
				this.numberOfLaserSystems = numberOfLaserSystems;
			}
		public int getPopulation()
			{
				return population;
			}
		public void setPopulation(int population)
			{
				this.population = population;
			}
		
	}
